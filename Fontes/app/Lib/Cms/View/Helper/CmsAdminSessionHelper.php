App::uses('SessionHelper', 'View/Helper');

class CmsAdminSessionHelper extends SessionHelper {



	public function flash($key = 'flash', $attrs = array()) {
		$out = false;

		if (CakeSession::check('Message.' . $key)) {
			$flash = CakeSession::read('Message.' . $key);
			$message = $flash['message'];
			unset($flash['message']);

			if (!empty($attrs)) {
				$flash = array_merge($flash, $attrs);
			}

			if ($flash['element'] == 'default') {
				$class = 'message';
				if (!empty($flash['params']['class'])) {
					$class = $flash['params']['class'];
				}
//				$out = '<div id="' . $key . 'Message" class="' . $class . '">' . $message . '</div>';
				$out = '<div id="' . $key . 'Message" class="' . $class . '">';
					$out .= '<div class="row" role="alert">';
						$out .= '<a href="#message" id="message">' . $message . '</a>';
		
						$modo_sistema = $this->requestAction(
							array(
								'ra' => true,
								'plugin' => 'usuarios', 
								'controller' => 'usuarios', 
								'action' => 'ra_modo_sistema',
								'admin' => true
							)
						);
						if($modo_sistema == MODO_SISTEMA_PADRAO) {
							$out .= '<a href="#closeMesage" id="closeMessage">Fechar mensagem</a>';
						}
					$out .= '</div>';
				$out .= '</div>';
			} elseif ($flash['element'] == '' || $flash['element'] == null) {
				$out = $message;
			} else {
				$options = array();
				if (isset($flash['params']['plugin'])) {
					$options['plugin'] = $flash['params']['plugin'];
				}
				$tmpVars = $flash['params'];
				$tmpVars['message'] = $message;
				$out = $this->_View->element($flash['element'], $tmpVars, $options);
			}
			CakeSession::delete('Message.' . $key);
		}
		return $out;
	}
}
